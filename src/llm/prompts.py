def format_state(compressed_state: str, history: List[Dict[str, Any]]) -> str:
    """Форматирует состояние для контекста LLM."""

    recent_history = history[-5:] if len(history) > 5 else history

    history_text = ""
    if recent_history:
        history_text = "\n\n**История последних действий:**\n"
        for i, item in enumerate(recent_history, 1):
            if "role" in item:
                role = "Агент" if item["role"] == "assistant" else "Пользователь"
                history_text += f"{i}. {role}: {item['content']}\n"
            elif "action" in item:
                action = item["action"]
                result = item["result"]
                success = "✓" if result.get("success") else "✗"
                params_str = str(action.get("params", {}))[:100]
                history_text += f"{i}. {success} {action['tool']}({params_str})\n"
                if not result.get("success") and result.get("error"):
                    history_text += f"   Ошибка: {result['error']}\n"

    return f"**Текущее состояние страницы:**\n{compressed_state}{history_text}"


